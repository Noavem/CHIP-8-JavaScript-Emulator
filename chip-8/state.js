export const state = {
    memory: new Uint8Array(4096),

    V: new Uint8Array(16),

    I: 0,

    pc: 0x200,

    currentInstruction: 0,

    stack: new Uint16Array(16),

    sp: 0,

    delayTimer: 0,
    soundTimer: 0,

    display: new Uint8Array(64 * 32),

    keys: new Uint8Array(16),

    spritesDrawnThisFrame: 0,

    lastKeyPressed: null
};

const fontSet = new Uint8Array([
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80 // F
]);

export function initializeFont() {
    for (let i = 0; i < fontSet.length; i++) {
        state.memory[0x050 + i] = fontSet[i];
    }
}

export function resetState() {
    state.memory.fill(0);
    state.V.fill(0);
    state.I = 0;
    state.pc = 0x200;
    state.stack.fill(0);
    state.sp = 0;
    state.delayTimer = 0;
    state.soundTimer = 0;
    state.display.fill(0);
    state.keys.fill(0);
    state.spritesDrawnThisFrame = 0;

    state.currentInstruction = 0;

    state.lastKeyPressed = null;

    initializeFont();
}